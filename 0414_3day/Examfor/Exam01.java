package Examfor;

public class Exam01 {

	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		for (int j = 21; j <= 57; j++) {
			System.out.println(j);
		}
		for (int x = 96; x >= 53; x--) {
			System.out.println(x);
		}
		// 중괄호 망가졌을때 전체선택(ctrl A + ctrl shift FF)
	}

}
